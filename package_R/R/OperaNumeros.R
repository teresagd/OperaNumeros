#' @title suma.dos.numeros
#' @description Funcion para realizar la suma de dos numeros
#' @param numeros sobre los que se desea obtener el resultado
#' @return El resultado de la operacion de sumar los numeros elegidos
#' @examples
#' suma.dos.numeros(5,5)
#'

suma.dos.numeros <- function(num1,num2){
     return (num1+num2)
}


#' @title multiplica.dos.numero
#' @description Funcion para realizar la multiplicacion de dos dos numeros
#' @param numeros sobre los que se desea obtener el resultado
#' @return El resultado de la operacion de multiplicar los numeros elegidos
#' @examples
#' multiplica.dos.numeros(4,6)
#'
multiplica.dos.numeros <- function(num1,num2){
  return (num1*num2)
}



